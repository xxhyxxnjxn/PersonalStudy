package com.spring.script.vo;

public class ScriptVo {
	private String scriptNo;
	private String side;
	private String side_num;
	private String log;
	public String getScriptNo() {
		return scriptNo;
	}
	public void setScriptNo(String scriptNo) {
		this.scriptNo = scriptNo;
	}
	public String getSide() {
		return side;
	}
	public void setSide(String side) {
		this.side = side;
	}
	public String getSide_num() {
		return side_num;
	}
	public void setSide_num(String side_num) {
		this.side_num = side_num;
	}
	public String getLog() {
		return log;
	}
	public void setLog(String log) {
		this.log = log;
	}
	
}
