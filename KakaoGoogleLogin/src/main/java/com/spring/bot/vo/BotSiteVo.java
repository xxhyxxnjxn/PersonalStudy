package com.spring.bot.vo;

public class BotSiteVo {
   private String site;
   private String site_kor;
   private String kind;
   public String getSite() {
      return site;
   }
   public void setSite(String site) {
      this.site = site;
   }
   public String getSite_kor() {
      return site_kor;
   }
   public void setSite_kor(String site_kor) {
      this.site_kor = site_kor;
   }
   public String getKind() {
      return kind;
   }
   public void setKind(String kind) {
      this.kind = kind;
   }
   
}